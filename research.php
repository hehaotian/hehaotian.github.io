        <b>First Language Acquisition: <br /> The Early Acquisition of Speech Prosody of Mandarin-Speaking Children (2012)</b><br /><br />
        <p>
        	<img style="width: 200px; float: right" src="img/tone.png"></img>
        	In this big research project, I am responsible for gathering, processing, and analyzing the Mandarin tones acquisition.<br /><br />
        	The neutral tone in Mandarin is a very important part, and children's neutral tone acquisition should also be paid attention. This study focuses on the duration of the neutral tone. According to phonetic production recorded from children's daily dialogue, we examine the length of its duration through three aspects, namely the position of the neutral tone, the words of different types with the neutral tone, and the neutral syllable with different vowels, and have drawn that D (middle) < D (end), D (measure word) < D (modal particles) < D (bi-multi-syllable words), and D (X+e5) < D (X+u5) < D (X+ii5) < D (X+a5). Furthermore, we discuss possible factors which influence its duration and the reasons why such phenomena happen.
        </p>
